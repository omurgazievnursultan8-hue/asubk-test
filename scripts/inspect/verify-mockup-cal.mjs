import { chromium } from 'playwright-core';
import { pathToFileURL } from 'url';
const file = process.argv[2] || 'mockups/decision/decisions.html';
const f = pathToFileURL(process.cwd() + '/' + file).href;
const ctx = await chromium.launchPersistentContext('.auth/profile', { channel: 'chrome', headless: true, ignoreHTTPSErrors: true, viewport: { width: 1700, height: 1100 } });
const page = ctx.pages()[0] || await ctx.newPage();
const log = (...a) => console.log(...a);
await page.goto(f, { waitUntil: 'networkidle' });
// reveal createView if present (decisions.html)
await page.evaluate(() => { const v = document.getElementById('createView'); if (v){ document.querySelectorAll('.view').forEach(s=>s.style.display='none'); v.style.display='flex'; } });
await page.waitForTimeout(400);
log('FILE:', file);
log('input value (prefill):', await page.locator('#decisionDate').inputValue());
await page.locator('#calToggle').click();
await page.waitForTimeout(300);
log('popup open:', await page.locator('.dp-pop.open').count() === 1);
log('title:', await page.locator('.dp-title').textContent());
log('weekdays:', JSON.stringify(await page.locator('.dp-wd').allTextContents()));
log('today cells:', JSON.stringify(await page.locator('.dp-day.today').allTextContents()));
log('sel cells:', JSON.stringify(await page.locator('.dp-day.sel').allTextContents()));
await page.screenshot({ path: '.auth/mockup-cal-open.png' });
await page.locator('.dp-day:not(.muted)', { hasText: /^7$/ }).first().click();
await page.waitForTimeout(200);
log('after pick 7 -> value:', await page.locator('#decisionDate').inputValue());
log('popup closed:', await page.locator('.dp-pop.open').count() === 0);
await page.locator('#calToggle').click(); await page.waitForTimeout(150);
await page.locator('.dp-nav[data-nav="-1"]').click(); await page.waitForTimeout(150);
log('after prev nav title:', await page.locator('.dp-title').textContent());
await ctx.close();
